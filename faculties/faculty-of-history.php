                На историческом факультете появляется специальность «Правоведение», а факультет стал называться историко-юридическим.
              </p>
              <p class="text-justify">
                С 2003 года исторический факультет функционирует самостоятельно. После образования Абхазского государственного университета предмет «История Абхазии» 
                становится вузовским. Если в пединституте имелись только две исторические кафедры (кафедра истории СССР и кафедра всеобщей истории),
                то в АГУ создается специальная кафедра по истории Грузии и Абхазии под названием кафедра истории СССР № 2, затем – кафедра истории Грузии и Абхазии. 
                После известных трагических событий 1989 года на основании решения учёного совета АГУ кафедра истории Грузии и Абхазии получает название кафедры
                истории, этнологии и археологии Абхазии, предложенное членами кафедры.
              </p>
              <p class="text-justify">
                У истоков факультета стояли выдающиеся ученые и видные общественные и государственные деятели – Дзидзария Г. А., Инал-ипа Ш. Д., Анчабадзе З. В., Хонелия А. Ф., 
                Адлейба А. К., Трапш М. М. Работали известные ученые-профессора – Амичба Г. А., Шамба Г. К., Воронов Ю. Н. Лидер национально-освободительного движения, 
                первый Президент Республики Абхазия Владислав Григорьевич Ардзинба был выпускником и профессором исторического факультета.
              </p>
              <p class="text-justify">
                С 2003 года на историческом факультете была введена новая перспективная специальность «Международные отношения», выпускники получают квалификацию специалиста в области
                международных отношений, предусматривающую выполнение функций референта, эксперта, консультанта в данной области; переводчика-референта в государственных органах, научных учреждениях.
              </p>
              <p class="text-justify">
