                В 1937 году в Сухумском государственном педагогическом институте был открыт исторический факультет. В 1938 году была создана кафедра истории, 
                которая и обеспечивала преподавание всех исторических дисциплин. В 1940 году на базе кафедры истории были образованы две самостоятельные кафедры: 
