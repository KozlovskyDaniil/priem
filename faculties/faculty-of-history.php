              </p>
              <p class="text-justify">
                В рамках направления «История» проводится обучение по четырем специализациям – история Абхазии, этнология Абхазии, археология Абхазии, история России и зарубежных стран,
                ведётся подготовка историков-магистров по проблемам древней, средневековой, новой и новейшей истории Абхазии.
              </p>
              <p class="text-justify">
                Приоритетным направлением на факультете является научно-исследовательская работа. Сотрудники кафедр работают над проблемами древней и средневековой археологии, этнологии,
                новой и новейшей истории Абхазии, истории национально-освободительного движения, по результатам работы издаются монографии и сборники научных трудов.
              </p>
              <p class="text-justify">
                Функционирует студенческое научное общество. Студенты участвуют в различных международных конференциях, симпозиумах, семинарах, круглых столах и т. д. Ежегодно проводятся
                Вороновские чтения, конференции, посвященные знаменательным событиям и видным деятелям. К чтению лекций на факультете также привлекаются ведущие преподаватели университетов Европы и России.
              </p>
              <p class="text-justify">
                Выпускники исторического факультета работают в системе образования, в научно-исследовательских институтах, организациях и учреждениях культуры, 
                экспертами в аналитических центрах, в общественных организациях, в органах государственного управления и местного самоуправления.
              </p>
              <p class="text-justify">
                Исторический факультет дает широкое профильное, квалифицированное образование и открывает перед студентами большие перспективы. Сотрудники и руководство факультета полны
                новых планов и идей по его развитию и введению новых специальностей в соответствии с требованиями времени. 
